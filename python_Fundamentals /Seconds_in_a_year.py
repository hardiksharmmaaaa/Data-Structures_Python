## WAP to Print the number of seconds in a year

seconds_in_a_minute=60
minutes_in_an_hour=60
hours_in_a_day=24
days_in_a_year=365

# calculate total seconds in a year
total_seconds=seconds_in_a_minute*minutes_in_an_hour*hours_in_a_day*days_in_a_year
print("Total seconds in a year is",total_seconds, "seconds")